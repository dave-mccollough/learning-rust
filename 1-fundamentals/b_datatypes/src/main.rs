fn main() {

// Decimals
    let x: i8 = 13;
    println!("{}", x);

    let y: u8 = 12;
    println!("{}", y);

    let decimal = 2_55;
    println!("{}", decimal);

    let hex = 0xff;
    println!("{}", hex);

    let octal = 0o377;
    println!("{}", octal);

    let binary = 0b1111_1111;
    println!("{}", binary);

// Floating Point

    let x = 2.0; //Floating point f64 - Default - better precision
    let y: f32 = 1.0;

// Boolean
    let t = true;
    let f: bool = false;

// Characters
    let c = "c";
    println!("{}", c);





}
